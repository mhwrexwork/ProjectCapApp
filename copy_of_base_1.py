def decode_batch_predictions(pred):
    input_len = ops.ones(pred.shape[0]) * pred.shape[1]

    results = keras.backend.ctc_decode(pred, input_length=input_len, greedy=True)[0][0]

    output_text = []
    for res in results:
        res = tf.gather(res, tf.where(tf.math.not_equal(res, -1)))
        output_text.append(tf.strings.reduce_join(num_to_char(res)).numpy().decode("utf-8"))
    return output_text


def levenshtein_distance(s1, s2):
    if len(s1) < len(s2):
        return levenshtein_distance(s2, s1)

    if len(s2) == 0:
        return len(s1)

    previous_row = range(len(s2) + 1)
    for i, c1 in enumerate(s1):
        current_row = [i + 1]
        for j, c2 in enumerate(s2):
            insertions = previous_row[j + 1] + 1
            deletions = current_row[j] + 1
            substitutions = previous_row[j] + (c1 != c2)
            current_row.append(min(insertions, deletions, substitutions))
        previous_row = current_row
    return previous_row[-1]


total_edit_distance = 0
total_chars = 0
num_samples = 0


prediction_model = keras.models.Model(
    inputs=[model.get_layer(name="image").output], outputs=model.get_layer(name="dense2").output
)

for batch in validation_dataset:
    images = batch["image"]
    labels = batch["label"]

    preds = prediction_model.predict(images)
    pred_texts = decode_batch_predictions(preds)

    for i in range(len(labels)):
        true_label = tf.strings.reduce_join(num_to_char(labels[i])).numpy().decode("utf-8")
        predicted_label = pred_texts[i]

        total_edit_distance += levenshtein_distance(true_label, predicted_label)
        total_chars += len(true_label)
        num_samples += 1

character_error_rate = total_edit_distance / total_chars if total_chars > 0 else 0
accuracy = (1 - character_error_rate) * 100

test_loss = model.evaluate(validation_dataset, verbose=2)

print("\nFinal Test Loss:", test_loss)
print("\nCharacter Error Rate (CER):", character_error_rate)
print("\nFinal Test Accuracy (1 - CER):", round(accuracy, 2), "%")