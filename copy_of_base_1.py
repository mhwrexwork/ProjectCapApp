class CTCLayer(layers.Layer):
  def __init__(self, name=None):
    super().__init__(name=name)
    self.loss_fn = keras.backend.ctc_batch_cost

  def call(self, y_true, y_pred):
    batch_len = tf.cast(tf.shape(y_true)[0], dtype="int64")
    input_length = tf.cast(tf.shape(y_pred)[1], dtype="int64")
    label_length = tf.cast(tf.shape(y_true)[1], dtype="int64")

    input_length = input_length * tf.ones(shape=(batch_len, 1), dtype="int64")
    label_length = label_length * tf.ones(shape=(batch_len, 1), dtype="int64")

    loss = self.loss_fn(y_true, y_pred, input_length, label_length)
    self.add_loss(loss)

    return y_pred

def build_model():
  input_img = layers.Input(
      shape=(img_width, img_height, 1), name= "image", dtype="float32"
  )
  labels = layers.Input(name="label", shape=(None,), dtype = "int64")

  x = layers.Conv2D(
      32,
      (3, 3),
      activation = "relu",
      kernel_initializer="he_normal",
      padding="same",
      name="Conv1"
  )(input_img)
  x = layers.BatchNormalization()(x)
  x = layers.MaxPooling2D((2, 2), name="pool1")(x)

  x = layers.Conv2D(
      64,
      (3, 3),
      activation = "relu",
      kernel_initializer="he_normal",
      padding="same",
      name="Conv2"
  )(x)
  x = layers.BatchNormalization()(x)
  x = layers.MaxPooling2D((2, 2), name="pool2")(x)

  new_shape = ((img_width // downsample_factor), (img_height // downsample_factor) * 64)
  x = layers.Reshape(target_shape=new_shape, name="reshape")(x)
  x = layers.Dense(64, activation="relu", name="dense1")(x)
  x = layers.BatchNormalization()(x)
  x = layers.Dropout(0.2)(x)

  x = layers.Bidirectional(layers.LSTM(128, return_sequences=True, dropout=0.25))(x)
  x = layers.Bidirectional(layers.LSTM(64, return_sequences=True, dropout=0.25))(x)

  num_classes = char_to_num.vocabulary_size() + 1
  x = layers.Dense(num_classes, activation="softmax", name="dense2")(x)

  output = CTCLayer(name="ctc_loss")(labels, x)

  model = keras.models.Model(
      inputs=[input_img, labels], outputs=output, name="ocr_model_v1"
  )

  opt = keras.optimizers.Adam(clipnorm=1.0)

  model.compile(optimizer=opt)
  return model

